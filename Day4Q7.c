// Write a program to swap two numbers without using a third variable.
#include <stdio.h>
int main()
{
    int a,b;
    printf("Enter number 1: ");
    scanf("%d", &a);
    printf("Enter number 2: ");
    scanf("%d", &b);
    a=a+b;
    b=a-b;
    a=a-b;
    printf("Swapped numbers are: %d,%d",a,b);
    return 0;
}
